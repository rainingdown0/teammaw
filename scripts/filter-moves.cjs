// 2. Map of specific Pokémon adjustments (Pokemon ID/Name -> Array of banned moves)
const SPECIFIC_EXCLUSIONS = {
  archaludon: ["bodypress"],
  incineroar: ["knockoff", "uturn"],
};

try {
  console.log("Reading learnsets.json...");
  const rawData = fs.readFileSync("../data/learnsets.json", "utf-8");
  const learnsets = JSON.parse(rawData);
  const filteredLearnsets = {};

  let totalOriginalMoves = 0;
  let totalFilteredMoves = 0;
  let manualExclusionsCount = 0;

  for (const [pokemon, moves] of Object.entries(learnsets)) {
    totalOriginalMoves += moves.length;

    // Normalize Pokémon string to match keys in SPECIFIC_EXCLUSIONS
    const normalizedPokemon = pokemon.toLowerCase().replace(/[^a-z0-9]/g, "");
    const bannedForThisPokemon = SPECIFIC_EXCLUSIONS[normalizedPokemon] || [];

    const validMoves = moves.filter((move) => {
      const normalizedMove = move.toLowerCase().replace(/[^a-z0-9]/g, "");

      // Check 1: Is it in the global allowed move pool?
      const isGloballyAllowed = CHAMPIONS_MOVES.has(normalizedMove);

      // Check 2: Is it explicitly banned for this specific Pokémon?
      const isSpeciallyBanned = bannedForThisPokemon.includes(normalizedMove);

      if (isGloballyAllowed && isSpeciallyBanned) {
        manualExclusionsCount++;
      }

      return isGloballyAllowed && !isSpeciallyBanned;
    });

    filteredLearnsets[pokemon] = validMoves;
    totalFilteredMoves += validMoves.length;
  }

  fs.writeFileSync(
    "../data/learnsets.json",
    JSON.stringify(filteredLearnsets, null, 2),
  );

  console.log("\n========================================");
  console.log("✅ Filter Complete!");
  console.log(`Processed ${Object.keys(learnsets).length} Pokémon.`);
  console.log(`Global pool filtration left: ${totalFilteredMoves} moves.`);
  console.log(`Targeted balance exclusions applied: ${manualExclusionsCount}`);
  console.log("========================================");
} catch (err) {
  console.error("❌ Error: ", err.message);
}
